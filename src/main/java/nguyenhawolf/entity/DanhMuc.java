package nguyenhawolf.entity;

public class DanhMuc {

	private int maDm;
	private String tenDm;
	private String img;
	public DanhMuc() {
		super();
	}
	public int getMaDm() {
		return maDm;
	}
	public void setMaDm(int maDm) {
		this.maDm = maDm;
	}
	public String getTenDm() {
		return tenDm;
	}
	public void setTenDm(String tenDm) {
		this.tenDm = tenDm;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}

}
