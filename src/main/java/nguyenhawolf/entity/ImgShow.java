package nguyenhawolf.entity;

public class ImgShow {

    private int maImg;
    private String thongTin;
    private int loaiAnh;
    private boolean hienThi;
    private String img;
    
	public ImgShow() {
		super();
	}
	public int getMaImg() {
		return maImg;
	}
	public void setMaImg(int maImg) {
		this.maImg = maImg;
	}
	public String getThongTin() {
		return thongTin;
	}
	public void setThongTin(String thongTin) {
		this.thongTin = thongTin;
	}
	public int getLoaiAnh() {
		return loaiAnh;
	}
	public void setLoaiAnh(int loaiAnh) {
		this.loaiAnh = loaiAnh;
	}
	public boolean isHienThi() {
		return hienThi;
	}
	public void setHienThi(boolean hienThi) {
		this.hienThi = hienThi;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
    

}
